AnimInfo& FindAnimInfo(ECharName Name, ECharAct action, ECharDir dir)
{
    switch (Name)
    {
    case ECharName::Marine:
        switch (action)
        {

        case ECharAct::Idle:
            for (int i = 0; i < 16; i++)
            {
                if (g_AnimInfo_Marin_Idle[i].dir == dir)
                    return g_AnimInfo_Marin_Idle[i];
            }
            break;

        case ECharAct::Move:
            for (int i = 0; i < 16; i++)
            {
                if (g_AnimInfo_Marin_Move[i].dir == dir)
                    return g_AnimInfo_Marin_Move[i];
            }
            break;

        case ECharAct::Attack:
            for (int i = 0; i < 16; i++)
            {
                if (g_AnimInfo_Marine_Attack[i].dir == dir)
                    return g_AnimInfo_Marine_Attack[i];
            }
            break;
        case ECharAct::Death:
            return g_AnimInfo_Marine_Death[0];
        }
        break;

    case ECharName::SCV:
        switch (action)
        {
        case ECharAct::Idle:
            for (int i = 0; i < 16; i++)
            {
                if (g_AnimInfo_SCV_Idle[i].dir == dir)
                    return g_AnimInfo_SCV_Idle[i];
            }
        case ECharAct::Move:
            for (int i = 0; i < 16; i++)
            {
                if (g_AnimInfo_SCV_Move[i].dir == dir)
                    return g_AnimInfo_SCV_Move[i];
            }
            break;
        case ECharAct::Gathering:
            for (int i = 0; i < 16; ++i)
            {
                if (g_AnimInfo_SCV_Gathering[i].dir == dir)
                    return g_AnimInfo_SCV_Gathering[i];
            }
            break;
        case ECharAct::Death:
            return g_AnimInfo_SCV_Death[0];
        case ECharAct::Attack:
            for (int i = 0; i < 4; ++i)
            {
                if (g_AnimInfo_SCV_Gathering[i].dir == dir)
                    return g_AnimInfo_SCV_Gathering[i];
            }
        }
        break;
    
    case ECharName::CommandCenter:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_CommandCenter_Idle[0];
        case ECharAct::Doing: return g_AnimInfo_CommandCenter_Doing[0];
        case ECharAct::BP:    return g_AnimInfo_CommandCenter_BP[0];
        case ECharAct::Death: return g_AnimInfo_CommandCenter_Death[0];
        }

    case ECharName::Academy:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_Academy_Idle[0];
        case ECharAct::BP:    return g_AnimInfo_Academy_BP[0];
        case ECharAct::Doing: return g_AnimInfo_Academy_Doing[0];
        }

    case ECharName::Barrack:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_Barrack_Idle[0];
        case ECharAct::BP:    return g_AnimInfo_Barrack_BP[0];
        case ECharAct::Doing: return g_AnimInfo_Barrack_Doing[0];
        }
    case ECharName::Bunker:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_Bunker_Idle[0];
        case ECharAct::BP:    return g_AnimInfo_Bunker_BP[0];
        }
    case ECharName::StarPort:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_StarPort_Idle[0];
        case ECharAct::BP:    return g_AnimInfo_StarPort_BP[0];
        case ECharAct::Doing: return g_AnimInfo_StarPort_Doing[0];
        }
    case ECharName::Armory:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_Armory_Idle[0];
        case ECharAct::BP:    return g_AnimInfo_Armory_BP[0];
        case ECharAct::Doing: return g_AnimInfo_Armory_Doing[0];
        }
    case ECharName::EngineeringBay:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_EngineeringBay_Idle[0];
        case ECharAct::BP:    return g_AnimInfo_EngineeringBay_BP[0];
        case ECharAct::Doing: return g_AnimInfo_EngineeringBay_Doing[0];
        }
    case ECharName::SupplyDepot:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_SupplyDepot_Idle[0];
        case ECharAct::BP:    return g_AnimInfo_SupplyDepot_BP[0];
        }
    case ECharName::Factory:
        switch (action)
        {
        case ECharAct::Idle:  return g_AnimInfo_Factory_Idle[0];
        case ECharAct::BP:    return g_AnimInfo_Factory_BP[0];
        case ECharAct::Doing: return g_AnimInfo_Factory_Doing[0];
        }
    case ECharName::ScienceFacility:
        switch (action)
        {
        case ECharAct::Idle:          return g_AnimInfo_ScienceFacility_Idle[0];
        case ECharAct::Doing:         return g_AnimInfo_ScienceFacility_Doing[0];
        case ECharAct::BP:            return g_AnimInfo_ScienceFacility_BP[0];
        }
    case ECharName::Refinery:
        switch (action)
        {
        case ECharAct::Idle:          return g_AnimInfo_Refinery_Idle[0];
        case ECharAct::BP:            return g_AnimInfo_Refinery_BP[0];
        }
        

    case ECharName::Cursor:
        switch (action)
        {
        case ECharAct::Idle:
            return g_AnimInfo_Cursor[0];
        case ECharAct::OnCursorTeam:
            return g_AnimInfo_Cursor[1];
        case ECharAct::OnCursorEnemy:
            return g_AnimInfo_Cursor[2];
        case ECharAct::Drag:
            return g_AnimInfo_Cursor[3];
        case ECharAct::Target:
            return g_AnimInfo_Cursor[4];
        default:
            break;
        }
    // 여기는 이펙트의 영역
    case ECharName::Effect:
    {
        switch (action)
        {
        case ECharAct::SCVSpark:
            return g_AnimInfo_Effect_SCVSpark[0];
        case ECharAct::MarineSpark:
            return g_AnimInfo_Effect_MarineSpark[0];
        default:
            break;
        }
    } break;

    case ECharName::LoadBar:
    {
        switch (action)
        {
        case ECharAct::Load:
            return g_AnimInfo_UnitLoadBar_Load[0];
        case ECharAct::Empty:
            return g_AnimInfo_UnitLoadBar_Empty[0];
        default:
            break;
        }
    } break;
    case ECharName::CarryMineral:
    {
        for (int i = 0; i < 16; ++i)
        {
            if (g_AnimInfo_CarryMineral[i].dir == dir)
                return g_AnimInfo_CarryMineral[i];
        }
    } break;
    case ECharName::TCarryGas:
        return g_AnimInfo_TCarryGas[0];

        break;
    }


    return g_AnimInfo_Effect_SCVSpark[0];

}

